class SizePrimitive
{
	public static void main(String[] args)
	{
		System.out.println("Size of byte: "+(Byte.SIZE/8)+ " byte");
		System.out.println("Size of Short: "+(Short.SIZE/8)+ " byte");
		System.out.println("Size of Integer: "+(Integer.SIZE/8)+ " byte");
		System.out.println("Size of Long: "+(Long.SIZE/8)+ " byte");
		System.out.println("Size of Character: "+(Character.SIZE/8)+ " byte");
		System.out.println("Size of Float: "+(Float.SIZE/8)+ " byte");
		System.out.println("Size of Double: "+(Double.SIZE/8)+ " byte");
	}
}